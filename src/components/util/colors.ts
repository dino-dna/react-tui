export const getRandom256 = () => {
  const keys = Object.keys(Colors256);
  const colorKey = keys[Math.floor(Math.random() * (keys.length - 1))] as keyof typeof Colors256;
  if (!colorKey) throw new Error(`unable to find color key`);
  return colorKey;
};

export const getColorCode = (color: keyof typeof Colors16 | keyof typeof Colors256) => {
  const code = Colors256[color as keyof typeof Colors256] || Colors16[color as keyof typeof Colors16];
  if (!code) throw new Error(`unable to find color: ${color}`);
  return code;
};
